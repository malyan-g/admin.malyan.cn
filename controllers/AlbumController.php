namespace app\controllers;

use Yii;
use yii\web\Response;
use app\models\Album;
use yii\web\UploadedFile;
use app\models\OperateLog;
use yii\bootstrap\ActiveForm;
use app\models\search\AlbumSearch;
use app\components\helpers\UploadHelper;

/**
 * 相册管理
 * Class AlbumController
 * @package app\controllers
 */
class AlbumController extends Controller
{
    /**
     * @inheritdoc
     */
    public function beforeAction($action)
    {
        $this->operateModule = OperateLog::EVENT_MODULE_ALBUM;
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }

    /**
     * 列表
     * @return string
     */
    public function actionList()
    {
        $searchModel = new AlbumSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);
        return $this->render('list',[
            'searchModel'=>$searchModel,
            'dataProvider'=>$dataProvider
        ]);
    }

    /**
     * 创建
     * @return string|\yii\web\Response
     */
    public function actionCreate()
    {
        return $this->save();
    }

    /**
     * 更新
     * @return string|\yii\web\Response
     */
    public function actionUpdate()
    {
        return $this->save(false);
    }

    /**
     * 详情
     * @return string
     */
    public function actionView()
    {
        return $this->render('view',[
            'model' => $this->findModel()
        ]);
    }

    /**
     * 删除
     * @return \yii\web\Response
     * @throws \Exception
     * @throws \Throwable
     */
    public function actionDelete()
    {
        $model = $this->findModel();
        if($model->delete()){
            $this->operateId = $model->id;
            $this->alert(Yii::t('common','Delete Successfully'), self::ALERT_SUCCESS);
        }else{
            $this->alert(Yii::t('common','Delete Failure'));
        }
        return $this->redirect(Yii::$app->request->referrer);
    }

    /**
     * 保存
     * @param bool $isCreate
     * @return string|\yii\web\Response
     * @throws \yii\web\NotFoundHttpException
     */
    private function save($isNewRecord = true)
    {
        $model = $isNewRecord ? new Album() : $this->findModel();
        
        $request = Yii::$app->request;
        if($request->isPost){
            if($request->isAjax && $model->load($request->post())){
                Yii::$app->response->format = Response::FORMAT_JSON;
                return ActiveForm::validate($model);
            }

            if($model->load($request->post())){
                // 传到七牛云
                $uploadedFile = UploadedFile::getInstance($model, 'cover');
                if($uploadedFile){
                    $model->cover = UploadHelper::qnUpload($uploadedFile->tempName, $uploadedFile->extension);
                }
                if($model->validate()){
                    if($model->save(false)){
                        $this->alert(Yii::t('common', $isNewRecord ? 'Create Successfully' : 'Update Successfully'), self::ALERT_SUCCESS);
                        $this->operateId = $model->id;
                        if($isNewRecord){
                            return $this->redirect('create');
                        }
                    }else{
                        $this->alert(Yii::t('common', $isNewRecord ? 'Create Failure' : 'Update Failure'));
                    }
                }
            }else{
                $this->exception(Yii::t('common', 'Illegal Operation'));
            }
        }
        return $this->render('form',[
            'model' => $model
        ]);
    }

    /**
     * 查询
     * @return  Album the loaded model
     * @throws \yii\web\NotFoundHttpException
     */
    public function findModel()
    {
        $id = (int) Yii::$app->request->get('id', 0);
        if($id){
            if(($model = Album::findOne($id)) !== null){
                return $model;
            }
        }
        $this->exception(Yii::t('common', 'Illegal Request'));
    }
}
